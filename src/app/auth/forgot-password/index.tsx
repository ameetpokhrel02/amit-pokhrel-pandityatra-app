import React, { useState } from 'react';
import { View, Text, StyleSheet, KeyboardAvoidingView, Platform, ScrollView } from 'react-native';
import { Image } from 'expo-image';
import { useRouter } from 'expo-router';
import { Button } from '@/components/ui/Button';
import { Input } from '@/components/ui/Input';
import { Colors } from '@/constants/Colors';

export default function ForgotPasswordScreen() {
  const router = useRouter();
  const [identifier, setIdentifier] = useState('');

  const handleSendOTP = () => {
    // TODO: Validate and send OTP
    router.push({ pathname: '/auth/otp-verify', params: { identifier, mode: 'reset-password' } } as any);
  };

  return (
    <KeyboardAvoidingView 
      behavior={Platform.OS === 'ios' ? 'padding' : 'height'}
      style={styles.container}
    >
      <ScrollView contentContainerStyle={styles.scrollContent}>
        <View style={styles.card}>
          <View style={styles.logoContainer}>
            <Image
              source={require('@/assets/images/pandit-logo.png')}
              style={styles.logo}
              contentFit="contain"
            />
          </View>

          <Text style={styles.title}>Forgot Password?</Text>
          <Text style={styles.subtitle}>Enter your email or phone number to receive a verification code.</Text>

          <Input
            label="Email or Phone"
            placeholder="Enter email or phone"
            value={identifier}
            onChangeText={setIdentifier}
            autoCapitalize="none"
          />

          <Button 
            title="Send Verification Code" 
            onPress={handleSendOTP} 
            style={styles.submitButton}
          />

          <View style={styles.footer}>
            <Text style={styles.link} onPress={() => router.back()}>Back to Login</Text>
          </View>
        </View>
      </ScrollView>
    </KeyboardAvoidingView>
  );
}

